package com.Day4;

import java.util.ArrayList;

public class ArrayListDemo1 {
    public static void main(String[] args) {
        ArrayList<String> color=new ArrayList<>();
        color.add("yellow");
        color.add("Green");
        color.add("Red");
        color.add("pink");

        for (String c:color){
            System.out.println(c);
        }





    }
}
