package com.Day1;

public class ForLoop {
    public static void main(String[] args) {
        for (int a = 0; a <= 15; a++) {
            System.out.println(a);
        }
 // initialze a variable; check condition; increment/decrement
        for (int a = 5; a > 0; a--) {
            //statement
            System.out.println(a);
        }
    }
}
